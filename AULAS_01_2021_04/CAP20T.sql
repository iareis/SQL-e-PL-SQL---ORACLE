CREATE OR REPLACE PROCEDURE CONSULTA_ALUNO2
(pREGISTRO IN OUT TALUNO%ROWTYPE)
IS 
    CURSOR CSQL IS
        SELECT * FROM TALUNO
        WHERE COD_ALUNO = pREGISTRO.COD_ALUNO;
BEGIN
    OPEN CSQL;
    FETCH CSQL INTO pREGISTRO;
    CLOSE CSQL;
END;





SELECT * FROM TALUNO;

