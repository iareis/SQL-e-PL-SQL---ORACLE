CREATE OR REPLACE PROCEDURE TUDODO_ALUNO
(pREGISTRO IN OUT TALUNO%ROWTYPE)
IS 
    CURSOR TUDO IS
        SELECT * FROM TALUNO
        WHERE COD_ALUNO = pREGISTRO.COD_ALUNO;
BEGIN
    OPEN TUDO;
    FETCH TUDO INTO pREGISTRO;
    CLOSE TUDO;
END;



