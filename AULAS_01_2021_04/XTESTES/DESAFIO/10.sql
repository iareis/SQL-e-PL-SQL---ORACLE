Exemplo de Cálculo do IPI
O IPI é calculado tendo como base a alíquota descrita na TIPI.
Para indústrias, o Imposto sobre Produtos Industrializados é calculado 
em relação ao valor da nota fiscal da mercadoria despachada. 

A NF pode eventualmente incluir valores sobre o frete e despesas como juros, 
taxas, entre outras.

Valor do IPI = Base de cálculo * (Alíquota / 100)

Sendo que:

Base de cálculo = (Valor do produto + Frete + Seguro + Outras Despesas Acessórias)

Observando a fórmula, você verá que descontos não são considerados na 
base de cálculo do IPI. Isso significa que se forem dados descontos 
quando o produto sofre tributação do IPI a empresa pagará tributos 
sobre a parte que sofreu desconto. Por isso, descontos nesse caso 
não são uma boa ideia. O melhor a fazer, nesta situação, é reduzir 
o preço unitário do item.

Importante também destacar que o valor do IPI é somado ao valor total 
do produto na linha da NF-e.

