A SQL possui a limitação de ser uma linguagem declarativa.
Isto significa que não é possível criar um programa inteiro em SQL,
pois a linguagem não possui comandos para tomada de decisão (ex: IF-ELSE)
e nem para execução de laços (ex: WHILE e FOR).

=========================================================================
INSTRUÇÃO	                        FUNÇÃO
------------------------------------------------------------------------
SELECT	                            Recuperação de dados.
INSERT UPDATE DELETE	            Manipulação de dados.
CREATE ALTER DROP RENAME TRUNCATE	Definição de objetos.
COMMIT ROLLBACK SAVEPOINT	        Controle de transações.
GRANT REVOKE	                    Controle de acesso.

=========================================================================

A PL/SQL pode ser entendida como uma extensão da linguagem SQL,
adicionada de funcionalidades que a tornam uma linguagem de programação completa: 
controle de fluxo, tratamento de exceções, orientação a objetos, entre outras. 

Com a PL/SQL podemos escrever programas inteiros, desde os mais simples até os mais sofisticados.
É uma tecnologia exclusiva Oracle, uma linguagem proprietária que pode ser utilizada apenas nos produtos desta empresa.

=========================================================================

SQL	                                                     PL/SQL
----------------------------------------------------------------------------------------------------------
Linguagem declarativa.                                   Linguagem imperativa.
Não é possível criar programas inteiros em SQL.	         Possibilita a criação de programas completos.   
Linguagem padrão ANSI.                                   Linguagem proprietária. 
Pode ser usada em qualquer SGBD relacional.	             Utilizada apenas pelo SGBD Oracle.   
